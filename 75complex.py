#Creating complex numbers
a=3+4j
b=2-5j
#Printing Complex Numbers
print("Complex number a:",a)
print("Complex number b:",b)
#Accessing real and imaginary parts
print("Real part of a:",a.real)
print("Imaginary part of a:",a.imag)
#Performing operations on complex numbers
print("Addition:",a+b)
print("Subtraction:",a-b)
print("Multiplication:",a*b)
#Using the complex()function
c=complex(7,-3)
print("Complex number c:",c)
#Finding the conjugate of a complex number
print("Conjugate of a:",a.conjugate())